void main() {
  // Basic Syntax
  int age = 24;
  print(age);

  String str = "Apple";
  print(str);

  double height = 5.5;
  print(height);

  bool isLoggedIn = true;
  print(isLoggedIn);

  // Variable
  print("\n");
  print("Variable Declaration\n");
  var language = "Dart";
  print(language);

  dynamic anything = 1000;
  print(anything);

  final pi = 3.1415;
  print(pi);

  const appName = 'MyApp';
  print(appName);
}
